package model;

import java.util.Date;

/**
 *
 * @author anh21
 */
public class CartItem {
    private Book book;
    private Date date;
    private int quantity;

    public CartItem() {
    }

    public CartItem(Book book, Date date, int quantity) {
        this.book = book;
        this.date = date;
        this.quantity = quantity;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
}
