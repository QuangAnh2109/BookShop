package model;

/**
 *
 * @author anh21
 */
public class CoverType {
    private int id;
    private String cover;

    public CoverType() {
    }

    public CoverType(int id, String cover) {
        this.id = id;
        this.cover = cover;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCover() {
        return cover;
    }

    public void setCover(String cover) {
        this.cover = cover;
    }
    
    
}
